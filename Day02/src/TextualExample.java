
public class TextualExample {

	public static void main(String[] args) {

		/*
		 # 단일 문자를 저장하는 데이터 타입 char
		 	저장하고 싶은 문자를 홑따옴표('')에 담아 대입
		 */
		char c1 = 'a';
		System.out.println(c1);
		
		char c2 = 65;
		System.out.println(c2);
		
		char c3 = 44032;
		System.out.println(c3);
		
		/*
		 # 문자열을 저장할 수 있는 데이터 타입 String
		 	저장하고 싶은 문자열을 겹따옴표("")에 담아 대입
		 	String은 기본 데이터 타입은 아니지만 자주 쓰이기 때문에
		 	기본 데이터 타입처럼 사용 (사실은 객체)
		 */
		
		String s1 = "my dream ";
		String s2 = "is a programmer";
		System.out.print(s1);
		System.out.print(s2+"\n");
		
		//자바에서는 문자열의 덧셈 연산을 지원함
		//연산 결과로 문자열을 연결하여 결합한 결과가 도출됨
		
		
		System.out.println(s1+s2+" hhhhh");
		
		//문자열과 다른 데이터 타입간의 덧셈 연산
		System.out.println("----------------------------------------------------");
		System.out.println(100+100);
		System.out.println("100"+"100");
		System.out.println(100+"100"); //전체 결과는 문자열로 됨
		System.out.println(3.14+"hi");
		
		int month = 10;
		int day = 13;
		
		//내 생일은 10월 13일 입니다.
		System.out.println("내 생일은 " + month + "월 " + day + "일 입니다."); // 다른 데이터 타입끼리 더해도 문자열이 되기 때문에 이렇게 사용 가능
		
		
		
		
		
		
		
		
		
		
		

	}

}
